"""Physics Projector — PhysDiff-inspired physics projection for 2D skeletal animation.

Inspired by PhysDiff (ICCV 2023), this module implements a lightweight 2D physics
engine based on Verlet integration and Position-Based Dynamics (PBD). It acts as a
post-processing layer that "projects" raw animation poses into a physically plausible
subspace, eliminating artifacts like foot sliding, ground penetration, and unnatural
stiffness.

**Critical design decision**: The existing renderer (`render_character_frame`) works
with angle-based pose dicts (`dict[str, float]`). This module therefore provides TWO
integration paths:

1. **AnglePoseProjector** (recommended): Works directly in angle space. It applies
   spring-damper physics to joint angles, producing smooth, physically-motivated
   secondary motion without breaking the renderer contract.

2. **PositionPhysicsProjector**: Works in position space using full Verlet integration
   and PBD. Converts corrected positions back to angles via inverse kinematics.
   More physically accurate but heavier.

Architecture overview (PhysDiff → 2D migration):
  1. Raw pose generated by existing animation system (presets.py, IK, etc.)
  2. Projector receives the raw pose and applies physics corrections
  3. Corrected pose returned as dict[str, float] for rendering

Mathematical foundations:
  - Verlet Integration: x(t+dt) = x(t) + (x(t) - x(t-dt)) + a(t)*dt²
  - PBD Distance Constraint: Δx = (|x2-x1| - L₀) / |x2-x1| * (x2-x1) * 0.5
  - Spring-Damper: F = -k*(θ - θ_target) - c*ω (angular spring)
  - Ground Constraint: if foot_y < ground_y then correct leg angles
  - Cognitive Motion: anticipation, follow-through, overlapping action

References:
  - PhysDiff: https://arxiv.org/abs/2212.02500
  - Jakobsen, "Advanced Character Physics" (GDC 2001)
  - Pikuma, "Verlet Integration and Cloth Physics Simulation"

Usage::

    from mathart.animation.physics_projector import AnglePoseProjector

    projector = AnglePoseProjector()

    # In the animation loop:
    raw_pose = idle_animation(t)
    corrected_pose = projector.step(raw_pose, dt=1/60)
    # corrected_pose is a dict[str, float] — same contract as raw_pose
"""
from __future__ import annotations

import math
from dataclasses import dataclass, field
from typing import Optional, Callable

import numpy as np


# ── Angular Spring-Damper State ───────────────────────────────────────────────


@dataclass
class AngularSpringState:
    """State of an angular spring-damper for a single joint.

    Uses the equation of motion:
        I * α = -k * (θ - θ_target) - c * ω + τ_ext

    where:
        I = moment of inertia
        k = angular spring stiffness
        c = angular damping coefficient
        θ = current angle
        ω = angular velocity (dθ/dt)
        θ_target = target angle from animation
        τ_ext = external torque (gravity, etc.)
    """
    angle: float = 0.0
    angular_velocity: float = 0.0
    prev_target: float = 0.0


@dataclass
class JointPhysicsConfig:
    """Physics configuration for a single joint.

    Parameters
    ----------
    spring_k : float
        Angular spring stiffness. Higher = snappier tracking.
        Range: 50-500 for primary joints, 10-50 for secondary.
    damping_c : float
        Angular damping. Higher = less oscillation.
        Range: 5-30.
    inertia : float
        Moment of inertia. Higher = more sluggish response.
        Range: 0.5-5.0.
    is_primary : bool
        If True, this joint closely follows the animation target.
        If False, it has more freedom to exhibit secondary motion.
    gravity_sensitivity : float
        How much this joint is affected by gravity torque.
        Range: 0.0-1.0. Arms/head should be higher.
    follow_through_strength : float
        Strength of follow-through effect (continuation after stop).
        Range: 0.0-1.0.
    anticipation_strength : float
        Strength of anticipation (brief reverse before main motion).
        Range: 0.0-0.3.
    overlap_delay : float
        Delay in seconds for overlapping action.
        Range: 0.0-0.1.
    """
    spring_k: float = 200.0
    damping_c: float = 15.0
    inertia: float = 1.0
    is_primary: bool = True
    gravity_sensitivity: float = 0.0
    follow_through_strength: float = 0.0
    anticipation_strength: float = 0.0
    overlap_delay: float = 0.0


